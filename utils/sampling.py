def dirichlet_split_noniid(dataset, n_clients, alpha):
    '''
    按照参数为alpha的Dirichlet分布将样本索引集合划分为n_clients个子集
    '''
    train_labels = np.array(dataset.targets)
    n_classes = train_labels.max()+1
    
    # (K, N) 类别标签分布矩阵X，记录每个类别划分到每个client去的比例
    label_distribution = np.random.dirichlet([alpha]*n_clients, n_classes)
    # (K, ...) 记录K个类别对应的样本索引集合
    class_idcs = [np.argwhere(train_labels == y).flatten()
                  for y in range(n_classes)]

    # 记录N个client分别对应的样本索引集合
    client_idcs = [[] for _ in range(n_clients)]
    for k_idcs, fracs in zip(class_idcs, label_distribution):
        # np.split按照比例fracs将类别为k的样本索引k_idcs划分为了N个子集
        # i表示第i个client，idcs表示其对应的样本索引集合idcs
        for i, idcs in enumerate(np.split(k_idcs,
                                          (np.cumsum(fracs)[:-1]*len(k_idcs)).
                                          astype(int))):
            client_idcs[i] += [idcs]

    client_idcs = [np.concatenate(idcs) for idcs in client_idcs]

    # #调节字体
    plt.rcParams['font.sans-serif'] = ['Times New Roman']  # 用来正常显示中文标签
    plt.rcParams['axes.unicode_minus'] = False  # 用来正常显示负号
    plt.rcParams['xtick.direction'] = 'in'#将x周的刻度线方向设置向内
    plt.rcParams['ytick.direction'] = 'in'#将y轴的刻度方向设置向内
    plt.figure(figsize=(12, 8))
    label_distribution = [[] for _ in range(n_classes)]
    for c_id, idc in enumerate(client_idcs):
        for idx in idc:
            label_distribution[train_labels[idx]].append(c_id)

    plt.hist(label_distribution, stacked=True,
                    bins=np.arange(-0.5, n_clients+1.5, 1),
                    label=["Class {}".format(i) for i in range(n_classes)], rwidth=0.5)
    # plt.xticks(np.arange(n_clients))
    plt.xlabel("Client ID",fontsize=15)
    plt.ylabel("Number of samples",fontsize=15)
    plt.legend()
    # plt.title("Label Distribution on Different Clients",fontsize=15)
    plt.savefig('./dataset_dis/image.pdf', dpi=500, bbox_inches = 'tight')
    plt.close()
    return client_idcs



